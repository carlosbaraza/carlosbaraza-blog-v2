interface SocialIconsProps {
  github?: string;
  twitter?: string;
  youtube?: string;
  linkedin?: string;
  email?: string;
  rss?: string;
  className?: string;
}

export function SocialIcons({
  github,
  twitter,
  youtube,
  linkedin,
  email,
  rss,
  className = "",
}: SocialIconsProps) {
  const links = [
    { href: github, Icon: GitHubIcon, label: "GitHub" },
    { href: twitter, Icon: TwitterIcon, label: "Twitter" },
    { href: youtube, Icon: YouTubeIcon, label: "YouTube" },
    { href: linkedin, Icon: LinkedInIcon, label: "LinkedIn" },
    {
      href: email ? `mailto:${email}` : undefined,
      Icon: EmailIcon,
      label: "Email",
    },
    { href: rss, Icon: RssIcon, label: "RSS Feed" },
  ].filter((link) => link.href);

  return (
    <div className={`flex items-center gap-3 ${className}`}>
      {links.map(({ href, Icon, label }) => (
        <a
          key={label}
          href={href}
          target={href?.startsWith("mailto:") ? undefined : "_blank"}
          rel={href?.startsWith("mailto:") ? undefined : "noopener noreferrer"}
          className="text-text-secondary hover:text-accent transition-colors"
          aria-label={label}
        >
          <Icon />
        </a>
      ))}
    </div>
  );
}
